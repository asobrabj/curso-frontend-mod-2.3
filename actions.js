export const soma = (numberA, numberB) => {
  return Number(numberA) + Number(numberB)
}

export const subtracao = (numberA, numberB) => {
  return Number(numberA) - Number(numberB)
}

export const multiplicacao = (numberA, numberB) => {
  return Number(numberA) * Number(numberB)
}

export const divisao = (numberA, numberB) => {
  return Number(numberA) / Number(numberB)
}
